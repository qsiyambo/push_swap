char	*ft_strrchr(char const *s, int c)
{
	int i;

	i = 0;
	while (s[i])
		i++;
	if (((c >= 32) && (c <= 126)) || (c == 0))
	{
		while ((i >= 0) || (c == 0))
		{
			if (s[i] == c)
				return ((char *)(s + i));
			else if (i == 0)
				return (0);
			i--;
		}
	}
	return (0);
}
